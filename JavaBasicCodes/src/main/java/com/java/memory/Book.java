package com.java.memory;

public class Book {
	private String author;
	private String title;
	public Book(String author, String title) {
		this.author = author;
		this.title = title;
	}
	
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	
	

}
